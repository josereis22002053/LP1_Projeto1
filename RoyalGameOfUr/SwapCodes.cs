namespace RoyalGameOfUr
{
    /// <summary>
    /// Codes that define which type of swap is made.
    /// </summary>
    public enum SwapCodes
    {

        /// <summary>
        /// Code to perform a normal swap.
        /// </summary>
        NormalSwap,

        /// <summary>
        /// Code to perform a swap when a player rolls a 0 on the dice.
        /// </summary>
        RolledZeroSwap
    }
}
